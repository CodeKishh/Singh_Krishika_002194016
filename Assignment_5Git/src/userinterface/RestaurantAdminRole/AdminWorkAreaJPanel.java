    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblOrder.getModel();
        
        model.setRowCount(0);
        
        for(Restaurant rest: ecosystem.getRestaurantDirectory().getRestaurant()){
            
            
    if(rest.getRestaurantName().equals(user.getUsername())) {
    for(Order foodMenu:rest.getOrderList()){
    
        Object[] row = new Object[5];
        row[0] = foodMenu;
        row[1] = foodMenu.getCustomerName();
        row[2] = foodMenu.getDeliveryLocation();
        row[3] = foodMenu.getCost();
        row[4] = foodMenu.getStatus();
        
    if(!foodMenu.getStatus().equals("Delivered")){
        
      model.addRow(row);
        
        
    }
    }
    
    }       
            
            
        }
    }
        
       
    
